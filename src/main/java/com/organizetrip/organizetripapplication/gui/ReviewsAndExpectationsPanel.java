    private void btnSendReviewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSendReviewMouseClicked
        String name = txtName.getText();
        String text = txtArea.getText().replaceFirst("Esperando comentario...", "");

        // Agrega la opinión a la lista
        reviewsControl.addReview(name, rating, text);

        // Reinicia los controles
        txtName.setText(userInfo.getName());
        rating = 1;
        txtArea.setText("Esperando comentario...");

        // Actualiza la lista de opiniones
        createReviewsList();
    }//GEN-LAST:event_btnSendReviewMouseClicked

    private void createReviewsList() {
        // Recibe la lista de opiniones almacenadas en el archivo reviews.json
        reviews = reviewsControl.getReviews();
        
        // Actualiza la lista de opiniones
        pnReviews.removeAll();
        reviews.forEach(review -> {
            ReviewPanel reviewPanel = new ReviewPanel(review);
            pnReviews.add(reviewPanel);
        });

        // Guarda la lista de reviews que pertenecen al usuario con nombre userInfo.getName()
        List<String> userReviews = new ArrayList<>();
        reviews.forEach(review -> {
            if (review.getName().equals(userInfo.getName())) {
                userReviews.add("[" + review.getDate() + " - " + review.getTime() + "] (" + review.getRating() + " estrellas): " + review.getComment());
            }
        });
        userInfo.setReviews(userReviews);
        
        // Actualiza la interfaz
        updateScrollPane();
    }

    private void updateScrollPane() {
        pnReviews.revalidate();
        pnReviews.repaint();
    
        // Calcular el tamaño preferido del panel basado en sus componentes
        int width = pnReviews.getPreferredSize().width;
        int height = 0;
        for (Component comp : pnReviews.getComponents()) {
            height += comp.getPreferredSize().height;
        }
        pnReviews.setPreferredSize(new Dimension(width, height));
    
        // Avisar al JScrollPane que verifique nuevamente si necesita mostrar barras de desplazamiento
        scrollPnReviews.revalidate();
        scrollPnReviews.repaint();
    }

