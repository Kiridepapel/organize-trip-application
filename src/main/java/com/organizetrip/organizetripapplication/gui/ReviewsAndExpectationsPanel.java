    private void selectRating() {
        List<JButton> stars = new ArrayList<>(
            List.of(btnStar1, btnStar2, btnStar3, btnStar4, btnStar5)
        );

        stars.forEach(star -> star.setIcon(new ImageIcon(getClass().getResource("/assets/images/star_off.png"))));

        for (int i = 0; i < rating; i++) {
            stars.get(i).setIcon(new ImageIcon(getClass().getResource("/assets/images/star.png")));
        }
    }

