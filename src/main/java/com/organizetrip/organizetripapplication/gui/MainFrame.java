    private void initPanels() {
        cardLayout = new CardLayout();
        containerPanel = new JPanel(cardLayout);
        getContentPane().add(containerPanel);
        
        WelcomePanel welcomePanel = new WelcomePanel(this, name);
        IndicatorPanel indicatorPanel = new IndicatorPanel(name);
        
        containerPanel.add(welcomePanel, "WelcomePanel");
        containerPanel.add(indicatorPanel, "IndicatorPanel");        
    }

    public void showCard(String card) {
        cardLayout.show(containerPanel, card);
        pack();
    }

